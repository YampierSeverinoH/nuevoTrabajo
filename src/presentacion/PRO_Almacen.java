private void cargarTrabajadores() throws Exception{
        TrabajadorDao objDAO = new TrabajadorDao();
        ArrayList <ClsTrabajador> lista;
        try{
            lista = objDAO.listadoTrabajdor();
            for(ClsTrabajador obj: lista) {
                this.jb_trabjador.addItem( obj );
            }
        } 
        catch (Exception ex) {
                throw ex;
        }
    }
private void cargarSucursal() throws Exception{
        SucursalDAO objDAO = new SucursalDAO();
        ArrayList <ClsSucursal> lista;
        try{
            lista = objDAO.listadoSucursal();
            for(ClsSucursal obj : lista){
                this.jb_sucursal.addItem( obj );
            }
        } 
        catch (Exception ex) {
                throw ex;
        }
    }
private void cargaAlmacen() throws Exception{
        AlmacenDao objDAO = new AlmacenDao();
        ArrayList<Almacen> lista;
        try{
            lista = objDAO.listadoAlmacen();
            for ( Almacen obj : lista ){
                String fila[] = { String.valueOf(obj.getIdAlmacen()), 
                                  obj.getDescripcion(),
                                  obj.getCodigo(),
                                  obj.getTelefono(), 
                                  String.valueOf(obj.getEstado()),
                                  obj.getDecEst(),
                                  String.valueOf(obj.getIdTrabajador()),
                                  String.valueOf(obj.getIdSucursal())};
                modelo.addRow(fila);
            }
        } 
        catch (Exception ex) {
            throw ex;
        }
    }
