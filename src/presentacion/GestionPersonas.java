    private void cmdGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdGrabarActionPerformed
        if ( this.txtNumero.getText().length() == 0  ){
             JOptionPane.showMessageDialog(rootPane, "Ingresar Numero");
             return;
        }
        //...
       
       Telefono tel;
       telefonoDAO objDAO = new telefonoDAO();
       char  princ;
       if (this.chkPrincipal.isSelected())
            princ = 0;
        else
            princ = 1;
       ClsPersona per = (ClsPersona) this.cbPersonas.getSelectedItem();
       int rbestado=0;
       if(rb_EsAct.isSelected()){
           rbestado=0;
       }else if(rb_EsIna.isSelected()){
           rbestado=1;
       }
       try{
           if (this.opc == true){        // cuando es nuevo
                tel = new Telefono( this.txtNumero.getText(),
                                       princ, 
                                       rbestado,                                   
                                       per.getIdPersona());
                objDAO.agregarTelefono(tel);
                JOptionPane.showMessageDialog(rootPane, "Telefono registrado");
            }
            else{        // cuando es actualizar
                tel = new Telefono( this.idSel,
                                       this.txtNumero.getText(),
                                       princ, 
                                       rbestado,                                   
                                       per.getIdPersona());
                objDAO.modificarTelefono(tel);
                JOptionPane.showMessageDialog(rootPane, "Telefono actualizado");
           }
            activar(false);
            modelo.setRowCount(0);
            cargarTelefonos();
        } 
       catch (Exception ex) {
            /*try {
                throw ex;
            } catch (Exception ex1) {
                Logger.getLogger(GestionPersonas.class.getName()).log(Level.SEVERE, null, ex1);
            }*/
       }
        
    }//GEN-LAST:event_cmdGrabarActionPerformed

    void buscarTelefono( String numero ) throws Exception{
        telefonoDAO objDAO = new telefonoDAO();
        ArrayList<Telefono> lista;
        try {
            lista = objDAO.buscarTelefono( numero );
            for ( Telefono obj : lista ){
                String fila[]= { String.valueOf( obj.getIdTelefono()), 
                                  obj.getNumero(), 
                                  String.valueOf(obj.getPrincipal()), 
                                  String.valueOf(obj.getEstado()),  
                                  String.valueOf(obj.getIdPersona())};
                modelo.addRow(fila);
            }
        } 
        catch (Exception ex) {
            throw ex;
        }
    }
        
    private void cmdBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdBuscarActionPerformed
        try{
            if ( this.txtBuscar.getText().length() == 0){
                JOptionPane.showMessageDialog(rootPane, "Ingresar Numero");
                return;
            }
            modelo.setRowCount(0);
            this.buscarTelefono( this.txtBuscar.getText());
        } 
        catch (Exception ex) {
            try {
                throw ex;
            } catch (Exception ex1) {
                Logger.getLogger(GestionPersonas.class.getName()).log(Level.SEVERE, null, ex1);
            }
        }
    }//GEN-LAST:event_cmdBuscarActionPerformed

    private void tbTelefonosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbTelefonosMouseClicked
        int fila = this.tbTelefonos.getSelectedRow();
        this.txtNumero.setText( modelo.getValueAt(fila, 1 ).toString() );
        if ("T".equals(modelo.getValueAt(fila, 2).toString()) )
            this.chkPrincipal.setSelected(true);
        else
            this.chkPrincipal.setSelected(false);
        this.txtEstado.setText( modelo.getValueAt(fila, 3).toString() );
        this.cbPersonas.setSelectedIndex( Integer.parseInt(modelo.getValueAt(fila, 4).toString()) - 1);
        this.idSel = Integer.parseInt(modelo.getValueAt(fila, 0).toString());
   
    }//GEN-LAST:event_tbTelefonosMouseClicked

