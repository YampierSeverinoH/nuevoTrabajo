    private void cargarPersonas() throws Exception{
        personaDAO objDAO = new personaDAO();
        ArrayList <ClsPersona> lista;
        try{
            lista = objDAO.listadoPersonas();
            for ( ClsPersona obj : lista ) {
                this.cbPersonas.addItem( obj );
            }
        } 
        catch (Exception ex) {
                throw ex;
        }
    }
    
    
    private void activar(boolean valor){
        this.txtNumero.setEnabled(valor);
        this.chkPrincipal.setEnabled(valor);
        this.txtEstado.setEnabled(valor);
        this.cbPersonas.setEnabled(valor);
        this.cmdGrabar.setEnabled(valor);
    }
    
    
    private void cargarTelefonos() throws Exception{
        telefonoDAO objDAO = new telefonoDAO();
        ArrayList<Telefono> lista;
        try{
            lista = objDAO.listadoTelefonos();
            for ( Telefono obj : lista ){
                String fila[] = { String.valueOf( obj.getIdTelefono()), 
                                  obj.getNumero(), 
                                  String.valueOf(obj.getPrincipal()), 
                                  String.valueOf(obj.getEstado()),  
                                  String.valueOf(obj.getIdPersona())};
                modelo.addRow(fila);
            }
        } 
        catch (Exception ex) {
            throw ex;
        }
    }
    
    
