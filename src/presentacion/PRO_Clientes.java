    private void cargarPersonas() throws Exception {
        personaDAO objDAO = new personaDAO();
        ArrayList<ClsPersona> lista;
        try {
            lista = objDAO.listadoPersonas();
            for (ClsPersona obj : lista) {
                this.cbPersonas.addItem(obj);
            }
        } catch (Exception ex) {
            throw ex;
        }

    }

