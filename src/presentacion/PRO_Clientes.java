    void buscarcliente(int idPersona) throws Exception {

        ClienteDAO objDAO = new ClienteDAO();
        ArrayList<ClsCliente> lista;
         limpiarTabla(tblcliente);
        try {
            lista = objDAO.buscarcliente(idPersona);
            for (ClsCliente obj : lista) {
                String fila[] = {String.valueOf(obj.getIdCliente()),
                    String.valueOf(obj.getIdPrestatario()),
                    String.valueOf(obj.getIdPersona()),
                    String.valueOf(obj.isEstado()),
                    obj.getFecha()};
                
                modelo.addRow(fila);
                

            }
        } catch (Exception ex) {
            throw ex;
        }
    }


