    private void listaMarca() {
        MarcaDAO objDAO = new MarcaDAO();
        ArrayList<ClsMarca> linea;
        try {

            linea = objDAO.listaMarca();
            for (ClsMarca obj : linea) {
                String fila[] = {String.valueOf(obj.getIdMarca()), obj.getDescripcionM()};
                modelo.addRow(fila);
//                cargarClientes();
            }

        } catch (Exception ex) {

        }

    }

    private void limpiarTabla(JTable tab) {
        DefaultTableModel tb = (DefaultTableModel) tab.getModel();
        int a = tab.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            tb.removeRow(tb.getRowCount() - 1);

        }

    }

